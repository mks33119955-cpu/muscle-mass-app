// ========== –ü–†–û–ì–†–ï–°–° ==========
function renderProgress() {
  const total = 30;
  let done = 0;
  for (let i = 1; i <= total; i++) {
    if (completedDays[i]) done++;
  }
  document.getElementById('progressBar').style.width = `${(done / total) * 100}%`;
}

// ========== –û–¢–û–ë–†–ê–ñ–ï–ù–ò–ï ==========
function renderDay(dayNum) {
  const w = workouts.find(item => item.day === dayNum);
  const m = meals.find(item => item.day === dayNum);
  if (!w || !m) return;

  document.getElementById('dayCounter').textContent = `–î–µ–Ω—å ${dayNum}`;
  document.getElementById('markDoneBtn').textContent = completedDays[dayNum] ? '‚úÖ –û—Ç–º–µ–Ω–∏—Ç—å' : '‚úÖ –í—ã–ø–æ–ª–Ω–µ–Ω–æ';
  document.getElementById('prevBtn').disabled = (dayNum <= 1);
  document.getElementById('nextBtn').disabled = (dayNum >= 30);

  // –£–ø—Ä–∞–∂–Ω–µ–Ω–∏—è
  let exHtml = '<ul>';
  w.exercises.forEach((ex, i) => {
    exHtml += `<li>${ex} <button class="how-btn" data-id="ex-${dayNum}-${i}">üîç</button></li>`;
  });
  exHtml += '</ul>';
  document.getElementById('workouts').innerHTML = `<div class="day-card"><h2>${w.title}</h2>${exHtml}</div>`;

  // –ï–¥–∞
  let totalKcal = m.meals.reduce((sum, meal) => sum + 950, 0);
  let mealHtml = `<div class="meal-plan"><h3>–ü–∏—Ç–∞–Ω–∏–µ ‚Äî ~${totalKcal} –∫–∫–∞–ª</h3>`;
  m.meals.forEach(meal => {
    mealHtml += `<div class="meal"><h4>${meal.type}</h4><p>${meal.desc}</p></div>`;
  });
  mealHtml += '</div>';
  document.getElementById('meals').innerHTML = mealHtml;

  renderProgress();
}

