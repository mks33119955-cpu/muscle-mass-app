let currentDay = 1;
const completedDays = {};
let waterGlasses = 0;
let lastResetDate = '';

// ========== –ó–ê–ì–†–£–ó–ö–ê ==========
function loadState() {
  const savedDay = localStorage.getItem('currentDay');
  if (savedDay) currentDay = parseInt(savedDay);

  const savedCompleted = localStorage.getItem('completedDays');
  if (savedCompleted) {
    try {
      const parsed = JSON.parse(savedCompleted);
      for (let key in parsed) {
        if (parsed[key] === true) completedDays[parseInt(key)] = true;
      }
    } catch (e) {}
  }

  // –í–æ–¥–∞: —Å–±—Ä–æ—Å –∫–∞–∂–¥—ã–π –¥–µ–Ω—å
  const today = new Date().toISOString().split('T')[0];
  lastResetDate = localStorage.getItem('waterResetDate') || '';
  if (lastResetDate !== today) {
    waterGlasses = 0;
    lastResetDate = today;
    localStorage.setItem('waterResetDate', today);
  } else {
    waterGlasses = parseInt(localStorage.getItem('waterGlasses')) || 0;
  }
}

// ========== –°–û–•–†–ê–ù–ï–ù–ò–ï ==========
