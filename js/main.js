document.addEventListener('DOMContentLoaded', () => {
  loadState();
  renderDay(currentDay);
  updateWaterUI();

  // –ù–∞–≤–∏–≥–∞—Ü–∏—è
  document.getElementById('prevBtn').addEventListener('click', () => {
    if (currentDay > 1) {
      currentDay--;
      saveState();
      renderDay(currentDay);
    }
  });

  document.getElementById('nextBtn').addEventListener('click', () => {
    if (currentDay < 30) {
      currentDay++;
      saveState();
      renderDay(currentDay);
    }
  });

  // –í—ã–ø–æ–ª–Ω–µ–Ω–æ
  document.getElementById('markDoneBtn').addEventListener('click', () => {
    if (completedDays[currentDay]) delete completedDays[currentDay];
    else completedDays[currentDay] = true;
    saveState();
    renderDay(currentDay);
  });

  // –°–±—Ä–æ—Å
  document.getElementById('resetBtn').addEventListener('click', () => {
    if (confirm('–°–±—Ä–æ—Å–∏—Ç—å –≤–µ—Å—å –ø—Ä–æ–≥—Ä–µ—Å—Å?')) {
      currentDay = 1;
      for (let i in completedDays) delete completedDays[i];
      waterGlasses = 0;
      saveState();
      renderDay(currentDay);
      updateWaterUI();
    }
  });

  // –í–æ–¥–∞
  document.getElementById('waterPlus').addEventListener('click', () => {
    if (waterGlasses < 16) waterGlasses++;
    saveState();
    updateWaterUI();
  });

  document.getElementById('waterMinus').addEventListener('click', () => {
    if (waterGlasses > 0) waterGlasses--;
    saveState();
    updateWaterUI();
  });

  document.getElementById('waterReset').addEventListener('click', () => {
    waterGlasses = 0;
    saveState();
    updateWaterUI();
  });
});
